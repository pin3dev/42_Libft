#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	int	cont;

	cont = 0;
	while (n > 0)
	{
		if (((unsigned char *) str)[cont] == (unsigned char)c)
			return ((unsigned char *)str + cont);
		cont++;
		n--;
	}
	return (NULL);
}
