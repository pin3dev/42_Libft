#include "../../inc/libft.h"

int	ft_atoi(const char *str)
{
	long	ret;
	int		sign;

	ret = 0;
	sign = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == 45 || *str == 43)
	{
		if (*str == 45)
			sign = -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		ret = ret * 10 + (*str++ - 48);
		if (ret * sign > 2147483647)
			return (-1);
		if (ret * sign < -2147483648)
			return (0);
	}
	return ((int)(ret * sign));
}
